package com.mgdsstudio.engine.nesgui;

public class KeyData {
    private int widthInUnits, heightInUnits;
    private String name;

    public KeyData(int widthInUnits, int heightInUnits, String name) {
        this.widthInUnits = widthInUnits;
        this.heightInUnits = heightInUnits;
        this.name = name;
    }


}
